@router.get("/preview/{assessment_id}")
async def preview_report(
    assessment_id: UUID,
    db: AsyncSession = Depends(get_db),
    current_user: PlatformUser = Depends(get_current_user),
):
    return await build_report_preview(assessment_id, db, current_user)


@router.post("/export")
async def export_report(
    request: ReportExportRequest,
    db: AsyncSession = Depends(get_db),
    current_user: PlatformUser = Depends(get_current_user),
):
    payload = await build_report_payload(request.assessment_id, db, current_user, request.sections)
    content, mime_type, encoding = _render_report(payload, request.format)
    filename = f"{_base_filename(payload, request.assessment_id)}.{request.format}"

    if isinstance(content, bytes):
        serialized_content = base64.b64encode(content).decode("ascii")
        content_encoding = "base64"
        byte_length = len(content)
    else:
        serialized_content = content
        content_encoding = encoding
        byte_length = len(content.encode("utf-8"))

    return {
        "filename": filename,
        "mime_type": mime_type,
        "content": serialized_content,
        "content_encoding": content_encoding,
        "byte_length": byte_length,
        "payload": payload,
    }


@router.post("/export-technique")
async def export_technique_note(
    request: TechniqueExportRequest,
    current_user: PlatformUser = Depends(get_current_user),
):
    """Accept command-deck technique export events from older UI cards.

    Full assessment reports are generated by `/reports/export`; this endpoint
    keeps standalone technique cards from failing when they record an export
    action outside an assessment report context.
    """
    return {
        "status": "accepted",
        "technique_id": request.technique_id,
        "title": request.title,
        "mitre_id": request.mitre_id,
    }


@router.post("/export/file")
async def export_report_file(
    request: ReportExportRequest,
    db: AsyncSession = Depends(get_db),
    current_user: PlatformUser = Depends(get_current_user),
) -> Response:
    """Return a direct downloadable report body.

    `/reports/export` keeps the existing JSON-wrapped export contract used by the
    web client. This endpoint exists for CLI/API callers that prefer a true file
    response and for future browser streaming flows.
    """
    payload = await build_report_payload(request.assessment_id, db, current_user, request.sections)
    content, mime_type, _encoding = _render_report(payload, request.format)
    filename = f"{_base_filename(payload, request.assessment_id)}.{request.format}"
    raw = content if isinstance(content, bytes) else content.encode("utf-8")
    return Response(
        content=raw,
        media_type=mime_type,
        headers={
            "Content-Disposition": f'attachment; filename="{filename}"',
            "X-AdByG0d-Report-Sections": ",".join(payload.get("report_meta", {}).get("sections", {}).get("included", [])),
        },
    )
