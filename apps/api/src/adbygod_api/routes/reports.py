router = APIRouter(prefix="/reports", tags=["reports"])


class ReportExportRequest(BaseModel):
    assessment_id: UUID
    format: str = Field("json", pattern="^(json|csv|html|pdf)$")
    sections: list[str] = Field(default_factory=list)


class TechniqueExportRequest(BaseModel):
    technique_id: str
    title: str | None = None
    mitre_id: str | None = None
    risk_level: str | None = None
    description: str | None = None


def _render_report(payload: dict[str, Any], fmt: str) -> tuple[str | bytes, str, str]:
    normalized = fmt.lower().strip()
    if normalized == "json":
        return json.dumps(payload, indent=2, ensure_ascii=True, default=str), "application/json", "utf-8"
    if normalized == "csv":
        return render_csv_report(payload), "text/csv", "utf-8"
    if normalized == "html":
        return render_html_report(payload), "text/html", "utf-8"
    if normalized == "pdf":
        return render_pdf_report(payload), "application/pdf", "binary"
    raise HTTPException(status_code=400, detail="Unsupported report format")


def _base_filename(payload: dict[str, Any], assessment_id: UUID) -> str:
    assessment = payload.get("assessment", {})
    domain = sanitize_filename_component(str(assessment.get("domain") or "assessment"))
    name = sanitize_filename_component(str(assessment.get("name") or "report"), fallback="report")
    return f"adbygod-{domain}-{name}-{assessment_id}"


